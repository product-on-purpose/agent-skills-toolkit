// check-generated-untracked.mjs - assert the generated Pattern S docs tree is never committed.
//
// WHAT IT IS: the R-SITE-2 guard for ADR 0024 D2. The site's published doc pages are GENERATED from
//   repo-root docs/ by gen-docs-site.mjs and rebuilt on every build; committing them would create a
//   drift surface (a stale generated page disagreeing with its source) the whole pattern exists to avoid.
// WHAT IT DOES: derives the emitted quadrants the same way the generator does (every subdir of docs/
//   except internal) and runs `git ls-files` over their site/src/content/docs/<quadrant>/ paths. Any
//   tracked file there is a committed generated page; the guard fails with the offending paths.
// WHY IT MATTERS: gitignore is the intent, but a stray `git add -f` or a new quadrant missing from
//   .gitignore would silently re-introduce the drift surface. This makes "generated, never committed"
//   a checked invariant, not a convention.
// WHAT USES IT: ci.yml's build-site job runs it on every PR; runs locally the same way.

import { execFileSync } from 'node:child_process';
import path from 'node:path';
import { fileURLToPath } from 'node:url';
import { emittedDirs } from './gen-docs-site.mjs';

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const REPO = path.resolve(SCRIPT_DIR, '..', '..');

const dirs = emittedDirs();
if (dirs.length === 0) {
  console.log('check-generated-untracked: no public docs quadrants found; nothing to guard.');
  process.exit(0);
}

const targets = dirs.map((d) => `site/src/content/docs/${d}/`);
let tracked = [];
try {
  const out = execFileSync('git', ['ls-files', '--', ...targets], { cwd: REPO, encoding: 'utf8' });
  tracked = out.split(/\r?\n/).map((s) => s.trim()).filter(Boolean);
} catch (err) {
  console.error(`check-generated-untracked: git ls-files failed: ${err.message}`);
  process.exit(1);
}

console.log('=== Generated Docs Untracked Check (R-SITE-2) ===');
console.log(`Guarded quadrants: ${dirs.join(', ')}`);

if (tracked.length === 0) {
  console.log('\nPASS: no generated docs pages are tracked (the Pattern S tree stays gitignored).');
  process.exit(0);
}

console.log(`\nFAIL: ${tracked.length} generated page(s) are tracked by git (they must be gitignored, not committed):`);
for (const f of tracked) console.log(`  - ${f}`);
console.log('\nThese are generated by site/scripts/gen-docs-site.mjs on every build. Remove them from git');
console.log('(git rm --cached) and confirm site/.gitignore ignores the quadrant, then rebuild.');
process.exit(1);
